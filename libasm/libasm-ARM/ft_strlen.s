.section __TEXT, __text
.global _ft_strlen          ; Делаем функцию глобальной

_ft_strlen:
    mov x1, x0              ; Копируем указатель на строку (x0) в x1
    mov x2, #0              ; Устанавливаем длину строки (счетчик) в 0

.loop:
    ldrb w3, [x1], #1       ; Загружаем байт из строки в w3, сдвигая указатель на 1
    cmp w3, #0              ; Проверяем, равен ли байт 0 (конец строки)
    beq .done               ; Если равен, выходим из цикла
    add x2, x2, #1          ; Увеличиваем длину строки
    b .loop                 ; Возвращаемся к следующему символу

.done:
    mov x0, x2              ; Возвращаем длину строки в x0
    ret                     ; Завершаем функцию